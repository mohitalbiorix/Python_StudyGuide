""" Python has a set of built-in math functions, including an extensive math module, that allows you to perform mathematical tasks on numbers. """

import math

#min and max
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

#abs
x = abs(-7.25);
print(x)

#pow
x = pow(4,3);
print(x)

#math module 
x = math.sqrt(64)
print(x)

y = math.ceil(1.4);
z = math.floor(1.4);
print(y);
print(z)

pi = math.pi;
print(pi)